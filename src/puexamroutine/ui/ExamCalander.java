    public void display( ExaminationDayCalander calander ){        
        java.util.Iterator<puexamroutine.control.domain.ExamDayIdentifier> ExamDayItr = calander.getCalanderIterator();
        int i=1;
        while( ExamDayItr.hasNext() ){
            puexamroutine.control.domain.Day ExamDay = calander.getExamDay( ExamDayItr.next() );
            puexamroutine.control.domain.CourseCode[] Exams = ExamDay.getExamCourses();
            String temp = getExams(Exams);
            Object[] array = new Object[]{ i+"", ExamDay.getShift(), temp };
            this.model.addRow(array);
            
            /*
             *  MEMORY BUG:: HERE, CALANDER.DAY WAS CHHECKED TO BE EQUAL TO EXAMDAY.GETSHIFT
             *  USING MEMORY ==.
             *  THIS WORKED FINE FOR THE STAND ALONE SYSTEM, BUT WHEN RESULT FROM OTHER SYSTEM WAS TO BE DISPLAYED THIS DIDNT WORKED
             * SO, THE ERROR WAS VERY SERIOUS CAUSING CALANDER TO BE DISPLAYED WRONGLY
             * SO, IT IS REPLACED BY EQUALS
             */
            if( ExamDay.getShift().equals( calander.DAY ) ){
                i++;
            }
        }
        this.TotalDaysText.setText( calander.getTotalExamDays()+"" );
    }
    
    public void display( ProgramRoutine prgRout ){
        this.Title.setText( this.Title.getText()+" for Program " + prgRout.getProgram().getProgramName() );
        java.util.Iterator<puexamroutine.control.domain.Day> DayItr = prgRout.getExamList().iterator();
        
        while( DayItr.hasNext() ){
            puexamroutine.control.domain.Day day = DayItr.next();
            CourseCode[] courses = prgRout.getExams(day).getCoursesForDay();
            Object[] array = { day.getExamID().getDate()+"", day.getShift(), this.getExams(courses)};
            this.model.addRow(array);            
        }
        
        this.TotalDaysText.setText( prgRout.getExamList().size() + "" );
    }
    
    public void display( GroupProgramRoutine GroupExamCalander ){
        this.Title.setText( this.Title.getText()+" for "+GroupExamCalander.getGroup().toString() );
        java.util.Iterator<puexamroutine.control.domain.Day> DayItr = GroupExamCalander.getExamDayList().iterator();
        
        while( DayItr.hasNext() ){
            puexamroutine.control.domain.Day day = DayItr.next();
            CourseCode[] courses = GroupExamCalander.getExamCourses(day).toArray( new CourseCode[]{} );
            Object[] array = { day.getExamID().getDate()+"", day.getShift(), this.getExams(courses)};
            this.model.addRow(array);            
        }
        
        this.TotalDaysText.setText( GroupExamCalander.getExamDayList().size() + "" );
    }
    
