    private String getExamsString( puexamroutine.control.domain.CourseCode[] array ){
        String val = "";
        for( int i=0; i<array.length; i++ ){
            if( i > 0 )
                val += "/";
            String course;
            if( this.Courses == null || this.Courses.get( array[i] ) == null || this.Courses.get( array[i] ).equals( "" ) )
                course = array[i].toString();
            else
                course = this.Courses.get( array[i] );
            val += course;
        }
        return val;
    }
    
