    private void removeAllCourses(){
        while( this.TableModel.getRowCount() > 0 ){
            this.TableModel.removeRow( 0 );
        }
        this.courseCounter = 0;
    }
    
    public boolean setCourses( puexamroutine.control.domain.list.CourseList courseMap, java.util.Collection<puexamroutine.control.domain.Group> grps ){
        try{            
            this.grps.clear();
            this.grps.addAll(grps);
            java.util.Iterator<puexamroutine.control.domain.Course> courseItr = courseMap.getCourses().iterator();
            while( courseItr.hasNext() ){
                puexamroutine.control.domain.Course c = courseItr.next();
                this.TableModel.addRow( new Object[]{ c.getCourseCode(), c.getCourseName(), c.getGroup() } );
                this.CourseEditor.addCourse(c.getCourseCode());
            }            
            return true;
        }
        catch( Exception e ){
            return false;
        }
    }

