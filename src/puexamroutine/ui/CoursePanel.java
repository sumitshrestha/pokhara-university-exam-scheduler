    private void addRow() {
        //if (this.isFilled()) { // dont use unfilled since it is not a compulsion to give the value for each course code.
            CourseInputForm tempForm = new CourseInputForm( null, this.grps );
            tempForm.setVisible( true );
            if( tempForm.getReturnStatus() == tempForm.RET_OK ){                                    
                if( ! this.DbWriter.addCourse( tempForm.getCourseCode().toString(), tempForm.getCourse().getCourseName(), tempForm.getCourse().getGroup().getFaculty(), tempForm.getCourse().getGroup().getLevel(), tempForm.getCourse().getGroup().getDiscipline()  ) )
                    JOptionPane.showMessageDialog(null, "course code cannot be added", "Error", JOptionPane.ERROR_MESSAGE);
                else{
                    ++this.courseCounter;
                    Object[] Value = { tempForm.getCourseCode().toString(), tempForm.getCourse().getCourseName(), tempForm.getCourse().getGroup().toString() };
                    this.TableModel.addRow(Value);
                    this.CourseEditor.addCourse( tempForm.getCourseCode() );
                }                
            }   
        //}
    }
    
    private boolean isFilled(){
        return this.courseTable.getRowCount() == 0 || !this.courseTable.getValueAt( courseTable.getRowCount() -1, 1 ).equals("");
    }
    
    private void delRow(){
        int index = this.courseTable.getSelectedRow();
        if (index > -1) {
            String c = this.courseTable.getValueAt( index, 0).toString();
            boolean state = this.DbWriter.deleteCourse( c );
            if( !state ){
                JOptionPane.showMessageDialog(null, c+" was not deleted", "Course Not deleted", JOptionPane.ERROR_MESSAGE);
                return;
            }
            this.TableModel.removeRow(index);
            this.courseCounter--;
            this.CourseEditor.onRowDeleted(index);
        } else {
            JOptionPane.showMessageDialog(null, "Please select row", "row not selected", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void onCourseNameChange( String CourseCode, String newCourseName ){
        try{
            if(this.DEBUG)System.out.println(CourseCode+" has been changed to "+newCourseName );
            boolean state=false;
            if( this.DbWriter.readCourses().keySet().contains( CourseCode ) ){//see if udpated course code is already present in course table
                state = this.DbWriter.updateCourseName(CourseCode, newCourseName);
            }
/*            
 * this code is cancelled since courses are added from this panel only.
 * so, no chance of courses not added.
 else{
                state = this.DbWriter.addCourse(CourseCode, newCourseName);                
            }*/
            if( ! state )
                JOptionPane.showMessageDialog( null, "The value could not be updated", "Error", JOptionPane.ERROR_MESSAGE );
        }
        catch( Exception e ){            
            JOptionPane.showMessageDialog( null, "The value could not be updated", "Error", JOptionPane.ERROR_MESSAGE );
        }
    }
    
