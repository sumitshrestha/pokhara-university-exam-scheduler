    private void initialize( java.util.HashMap<Group, ProgramList > list ){
        while( model.getRowCount() > 0 ){
            this.model.removeRow( 0 );
        }
        
        java.util.Iterator<Group> grpItr = list.keySet().iterator();
        while( grpItr.hasNext() ){
            Group grp = grpItr.next();
            java.util.Iterator<Program> prgItr = list.get(grp).getPrograms().iterator();
            while( prgItr.hasNext() ){
                Program prg = prgItr.next();
                ProgramColleges colls = new ProgramColleges(prg);
                colls.addColleges( prg.getSupportingColleges() );
                this.model.addRow( new Object[]{ grp, prg, colls } );
            }
        }
    }
    
    private void updateProgramColleges(){
        final int row = this.prgTable.getSelectedRow();
        if( row == -1 ){
            JOptionPane.showMessageDialog( null, "Please Select Program/Row from Table", "Program Not Selected", JOptionPane.ERROR_MESSAGE );            
            return;
        }
        
        ProgramColleges colls = (ProgramColleges) this.model.getValueAt( row, 2 );
        Program prg = colls.getProgram();
        Group grp = prg.getGroup();
        try{
            grp = this.controller.getGroupList().getGroup( grp.getFaculty(), grp.getLevel(), grp.getDiscipline() );
        }
        catch( Exception e ){
            System.out.println( "Proper Group could not be obtained" );
        }
        CollegesForm form = new CollegesForm( null, prg, this.centres, this.controller.getGroupList().getCoursesMap( grp ) );
        form.setVisible( true );
        if( form.getReturnStatus() == form.RET_OK ){
            java.util.Iterator<College> prgColls = colls.getProgramColleges().iterator();
            while( prgColls.hasNext() ){
                College c = prgColls.next();
                boolean st = this.writer.removeCollegeProgram( c.getCollegeName(), prg.getProgramName() );
                if( ! st ){
                    JOptionPane.showMessageDialog( null, c.getCollegeName()+" could not be removed", "Error while updating", JOptionPane.ERROR_MESSAGE );
                }
            }            
            Program newProgram = form.getProgram( grp );
            ProgramColleges newPrgColls = new ProgramColleges(newProgram);
            newPrgColls.addColleges( newProgram.getSupportingColleges() );
            boolean st = this.controller.addProgramColleges(newProgram);
            if( st )
                this.model.setValueAt( newPrgColls, row, 2  );            
            else{
                JOptionPane.showMessageDialog( null, "The program could not be updated", "Program Update Error", JOptionPane.ERROR_MESSAGE );
            }
        }
    }
    
    private void updateProgram(){
        final int row = this.prgTable.getSelectedRow();
        if( row == -1 ){
            JOptionPane.showMessageDialog( null, "Please Select Program/Row from Table", "Program Not Selected", JOptionPane.ERROR_MESSAGE );            
            return;
        }
        
        final Program prg = (Program) this.model.getValueAt( row, 1 );
        
        final String fac = prg.getGroup().getFaculty();
        final String level = prg.getGroup().getLevel();
        final String discp = prg.getGroup().getDiscipline();
        
        updateProgramForm form = new updateProgramForm( null, fac, level, discp, prg.getProgramName() );
        form.setVisible( true );
        if( form.getReturnStatus() == form.RET_OK ){
            if( this.writer.updateProgram( prg.getProgramName(), fac, level, discp, form.getText() )){
                boolean st = prg.rename( form.getText() );
                if( ! st ){
                    JOptionPane.showMessageDialog( null, "The program Name could not be updated", "Program Update Error", JOptionPane.ERROR_MESSAGE );
                }
                final Program prg1 = ((ProgramColleges) this.model.getValueAt( row, 2 )).getProgram();
                st = prg1.rename( form.getText() );
                if( ! st ){
                    JOptionPane.showMessageDialog( null, "The program Name could not be updated", "Program Update Error", JOptionPane.ERROR_MESSAGE );
                }
            }
            else
                JOptionPane.showMessageDialog( null, prg+" cannot be updated", "update Program error", JOptionPane.ERROR_MESSAGE );
        }
    }
    
    private void updateGroup(){
        final int row = this.prgTable.getSelectedRow();
        if( row == -1 ){
            JOptionPane.showMessageDialog( null, "Please Select Program/Row from Table", "Program Not Selected", JOptionPane.ERROR_MESSAGE );            
            return;
        }        
        final Program prg = (Program) this.model.getValueAt( row, 1 );        
        java.util.Collection<Group> grps = this.controller.getGroupList().getGroups();
        ProgramGroupInputForm form = new ProgramGroupInputForm( null, prg, grps );
        form.setVisible( true );
        if( form.getReturnStatus() == form.RET_OK ){
            Group selectGrp = form.getGroup();
            boolean st = this.writer.updateProgramGroup( prg.getProgramName(), selectGrp.getFaculty(), selectGrp.getLevel(), selectGrp.getDiscipline() );
            if( ! st ){
                JOptionPane.showMessageDialog( null, prg.getProgramName()+" could not be updated", "Program Not Updated", JOptionPane.ERROR_MESSAGE );
            }
            else{
                this.model.setValueAt( selectGrp, row, 0 );
                ((Program)this.model.getValueAt(row, 1 )).setGroup(selectGrp);
                ((ProgramColleges)this.model.getValueAt(row, 2 )).getProgram().setGroup(selectGrp);
            }
        }
    }
    
    private void addProgram(){
        ProgramGroupInputForm form = new ProgramGroupInputForm( null, null, this.controller.getGroupList().getGroups() );
        form.setVisible( true );
        if( form.getReturnStatus() == form.RET_OK ){
            Group grp = form.getGroup();
            String prg = form.getProgramName();
            Program newProg = new Program( prg, grp );
            CollegesForm collform = new CollegesForm( null, newProg, this.centres, this.controller.getGroupList().getCoursesMap( grp ) );
            collform.setVisible( true );
            if( collform.getReturnStatus() == collform.RET_OK ){
                newProg = collform.getProgram(grp);
                boolean st = this.controller.addProgram(newProg);
                if( st ){
                    this.model.addRow( new Object[]{ grp, newProg, new ProgramColleges(newProg, newProg.getSupportingColleges() ) } );
                }
                else{
                    JOptionPane.showMessageDialog( null, "Program "+newProg.getProgramName()+" could not be added in database.", "Error Progarm Addition", JOptionPane.ERROR_MESSAGE );
                }
            }        
        }
    }
    
    private void deleteProgram(){
        final int row = this.prgTable.getSelectedRow();
        if( row == -1 ){
            JOptionPane.showMessageDialog( null, "Please Select Program/Row from Table", "Program Not Selected", JOptionPane.ERROR_MESSAGE );            
            return;
        }
        final Program prg = (Program) this.model.getValueAt( row, 1 );
        final Group grp = prg.getGroup();
        int op = JOptionPane.showConfirmDialog( null, "Are you sure you want to delete Program "+prg.getProgramName()+"?", "Program Deletion Confirmation",JOptionPane.YES_NO_OPTION );
        if( op == JOptionPane.NO_OPTION ){
            return;
        }
        boolean st = this.writer.removeProgram( prg.getProgramName(), grp.getFaculty(), grp.getLevel(), grp.getDiscipline() );
        if( st ){
            this.model.removeRow(row);
        }
        else{
            JOptionPane.showMessageDialog( null, "Program "+ prg.getProgramName()+" could not be deleted from database.", "Error Progarm Deletion", JOptionPane.ERROR_MESSAGE );
        }
    }
    
