    /**
     * This class represents the colleges of a particular program.
     * 
     * @author Sumit Shresth
     */
    class ProgramColleges implements java.io.Serializable{
        
        public ProgramColleges( Program prg ){
            this.prg = prg;
        }
        
        public ProgramColleges( Program prg, java.util.Collection<College> coll ){
            this( prg );
            this.addColleges(coll);
        }
        
        public void addColleges( java.util.Collection<College> coll ){
            this.PrgColleges.addAll(coll);
        }
        
        public void addCollege( College coll ){
            this.PrgColleges.add(coll);
        }
        
        @Override
        public final String toString(){
            java.util.Iterator<College> collItr = this.PrgColleges.iterator();
            String ret = "";
            while( collItr.hasNext() ){
                ret += collItr.next().getCollegeName() + ",";
            }
            ret = ret.substring( 0, ret.length() - 1 );
            return ret;
        }
        
        public final java.util.Collection<College> getProgramColleges(){
            return this.PrgColleges;
        }
        
        public final Program getProgram(){
            return this.prg;
        }
        
        private final Program prg;
        java.util.Collection<College> PrgColleges = new java.util.ArrayList<College>();
    }
    
