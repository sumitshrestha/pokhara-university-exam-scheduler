    private void onSuccessfullScheduling( Result rs ) throws HeadlessException {
        if (this.RoutineTree == null) {
            this.RoutineTree = new ExamRoutineTree(this.centralTab);
            this.TreeTab.add("Routine", this.RoutineTree);
        }
        if( this.DEBUG )
            rs.getExamCalander().print();
        this.TreeTab.setSelectedComponent(this.RoutineTree);
        this.RoutineTree.setRoutines( rs.getExamCalander(), rs.getGroupRoutine(), this.controller.getGroupList().getCourses_Code_Name_Mapping() );
        this.TreeViewCombo.setSelected( true );
        Collection<puexamroutine.control.domain.Group> unsheduledgrps = rs.getunScheduledGroups();        
        if (unsheduledgrps.isEmpty()) {
            JOptionPane.showMessageDialog(null, "The value was successfully scheduled...");
        } else {
            Iterator<puexamroutine.control.domain.Group> grpitr = unsheduledgrps.iterator();
            String grps = "";
            while (grpitr.hasNext()) {
                puexamroutine.control.domain.Group grp = grpitr.next();
                grps += grp.getFaculty() + ":" + grp.getLevel() + ":" + grp.getDiscipline() + "\n";
            }
            JOptionPane.showMessageDialog(null, "Following could not be scheduled\n" + grps + "try increasing maximum days or decreasing minimum days", "Error", JOptionPane.ERROR_MESSAGE);
        }        
    }
    
