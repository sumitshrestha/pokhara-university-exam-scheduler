    private void makeRemoteSchedule( boolean onCancel ){
        if( ! this.remoteSchedulingCheck.isSelected() )    {
            this.remoteSchedulingEditMenu.setEnabled( false );
            return;    
        }
        this.remoteScheduleForm.setVisible( true );
        if( this.remoteScheduleForm.getReturnStatus() == this.remoteScheduleForm.RET_OK ){
            this.controller.setRemoteSchedulerIP( this.remoteScheduleForm.getIP() );
            this.controller.setRemoteSchedulerPort( this.remoteScheduleForm.getPortNo() );
            this.remoteSchedulingEditMenu.setEnabled( true );
        }
        else{
            if( onCancel )
                this.remoteSchedulingCheck.setSelected( false );
        }
    }
    
    private SwingWorker<Result, Void> getSchedulerThread(){
        return new SwingWorker<Result, Void>(){        
        
            @Override
        public Result doInBackground(){                
            controller.reRead();            
            Result rs = controller.start( remoteSchedulingCheck.isSelected() );                        
            return rs;
        }   
            
            @Override
        public void done(){                
        try{
            Result state = this.get();
            monitor.setVisible( false );            
            
            if( onSystemError() )return;
            
            if( state.executedSuccessfully() ){                            
                onSuccessfullScheduling( state );                                
            }
            else
                JOptionPane.showMessageDialog( null, state.getError(), "Error", JOptionPane.ERROR_MESSAGE );
        }
        catch( Exception e ){                    
            System.out.println( "error in done "+e.getMessage() );
        }
        }
        };        
    }
    
    private boolean onSystemError() {
        if (SystemError) {
            this.dispose();
            return true;
        }
        return false;
    }
    
