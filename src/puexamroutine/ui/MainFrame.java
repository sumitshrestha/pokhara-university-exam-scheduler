    public void initialize() {
        if (this.initializeBackEnds()) {
            this.DataTree.initialize(writer);
            this.DataTree.setDataLoader(controller);
            this.CenterTree = new ExamCentreTree(this.writer);
            this.CenterTree.setTreeList(this.controller.getCentres());
            this.CentreTreePanel.add(this.CenterTree);            
            
            // initialize form view components
            this.grpPanel.initialize(controller);
            this.ExamCentrePanel.setDataLoader(writer);
            this.prgPanel.initialize(controller);
            
            this.initializeCoursePanel();
        } else {
            this.disableAll();
            this.setVisible(true);            
            int result = JOptionPane.showConfirmDialog( null, "The database could not be properly connected.\nDo You Want to TroubleShoot it OR Close\nPress OK to continue and Cancel to close", "Confirm Continue", JOptionPane.YES_NO_OPTION );
            if( result == JOptionPane.NO_OPTION ){
                System.exit( 0 );
            }
            this.DatabaseSettings.showDatabaseSettings();
            if( this.DatabaseSettings.isChanged() ){
                //System.out.println("changed...");
                this.initialize();            
                this.enableAll();
            }/*
            else
                System.out.println("not changed...");*/
        }
    }
    
