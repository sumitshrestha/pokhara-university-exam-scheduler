    private void addRow() {
        if (this.isFilled()) {
            Object[] array = {""};
            this.model.addRow(array);
        }
    }

    private void deleteRow() throws HeadlessException {
        int index = this.table.getSelectedRow();
        if (index > -1) {
            this.model.removeRow(index);
            this.editor.onRowDeleted(index);
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Please select college to be deleted", "College Row not Selected...", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
    
