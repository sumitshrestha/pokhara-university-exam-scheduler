    private void tableKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tableKeyPressed
        final int DOWN_KEY = 40;
        if( evt.getKeyChar() == java.awt.event.KeyEvent.VK_DELETE ){
            this.deleteRow();
            return;
        }
        if( this.table.getSelectedRow() < this.table.getModel().getRowCount() - 1 ){
            System.out.println( "returning..." );
            return;
        }
        if( evt.getKeyCode() == DOWN_KEY ){
            this.addRow();
        }   
    }//GEN-LAST:event_tableKeyPressed
    
    private void doClose(int retStatus) {
        returnStatus = retStatus;
        setVisible(false);
        dispose();
    }
    
    private boolean isFilled(){
        return this.table.getRowCount() < 1 || !this.table.getValueAt( this.table.getRowCount() - 1, 0).equals("");
    }
    
    public void setProgram( final String prg ){
        this.Program = prg;
        this.programText.setText(prg);
        this.editor.setProgram(prg);
        if( prg == null )
            this.programText.setEditable( true );        
        else
            this.programText.setEditable( false );
    }
    
    public void setFaculty( final String Fac ){
        if( this.FacCombo.getItemCount() > 0 ){
            this.FacCombo.removeItemAt( this.FacCombo.getSelectedIndex() );
        }
        this.FacCombo.addItem( Fac );
    }
    
    public void setLevel( final String level ){
        if( this.LevelCombo.getItemCount() > 0 ){
            this.LevelCombo.removeItemAt( this.LevelCombo.getSelectedIndex() );
        }
        this.LevelCombo.addItem( level );
    }
    
    public void setDiscipline( final String Disc ){
        int index = this.DisciplineCombo.getSelectedIndex();
        if( index >= 0 )
            this.DisciplineCombo.removeItemAt( index );
        
        this.DisciplineCombo.addItem( Disc );
    }
    
    public Program getProgram( Group grp ){
        Program prg = new Program( this.Program,grp );
        Iterator<CollegeForm> forms = this.editor.getForms().iterator();
        while( forms.hasNext() ){
            prg.addCollege( forms.next().getCollege(prg) );
        }
        return prg;
    }
    
    public String[] getColleges(){        
        Iterator<CollegeForm> collItr = this.editor.getForms().iterator();
        java.util.ArrayList<String> list = new java.util.ArrayList<String>();
        while( collItr.hasNext() ){
            list.add( collItr.next().getCollegeName() );
        }
        return list.toArray( new String[]{} );
    }
    
    @Override
    public String toString(){
        String[] array = this.getColleges();
        String collstr = "";
        for( int i=0; i<array.length; i++ ){
            collstr += array[i];
            if( i < array.length -1 )
                collstr += ",";
        }
        return collstr;
    }
    
