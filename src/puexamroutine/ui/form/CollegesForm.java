    public CollegesForm(java.awt.Frame parent, final Program prg1, String[] centres, java.util.Map<CourseCode,String> courses  ) {
        this( parent, prg1.getProgramName(), prg1.getGroup().getFaculty(), prg1.getGroup().getLevel(), prg1.getGroup().getDiscipline(), centres, courses );
        
        java.util.List<College> coll = java.util.Arrays.asList( prg1.getSupportingColleges().toArray( new College[]{} ) );
        this.editor.addColleges( prg1, coll );
        java.util.ListIterator<College> collItr = coll.listIterator();
        while( collItr.hasNext() ){
            College c = collItr.next();
            this.model.addRow( new Object[]{c.getCollegeName()} );
        }
    }
    
