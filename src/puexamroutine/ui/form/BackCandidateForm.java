    public CourseCode[] getCourses(){
        try{
            List<CourseCode> list = this.CourseEditor.getForms();            
            return list.toArray( new CourseCode[]{} );
        }
        catch( Exception e ){
            return null;
        }
    }
    
    private boolean isfilled(){
        return this.model.getRowCount() < 1 || !this.model.getValueAt( this.model.getRowCount()-1, 0 ).equals("");
    }
    
    public void setCandidateID( String ID ){
        this.ID = ID;
        int selectindx = this.cand.getSelectedIndex();
        if( selectindx >= 0 )
            this.cand.removeItemAt( selectindx );
        this.cand.addItem( ID );
    }
    
    public final String getCandidateID(){
        return this.cand.getSelectedItem().toString();
    }
    
