    private boolean addRow() {
        if( this.ERROR_MESSAGE != null ){
            javax.swing.JOptionPane.showMessageDialog(null, this.ERROR_MESSAGE, "Course Code Not present", javax.swing.JOptionPane.ERROR_MESSAGE);
            return true;
        }
        
        if (!this.isfilled()) {
            return true;
        }
        
        this.model.addRow(new String[]{""});
        return false;
    }

    private void delRow() throws HeadlessException {
        int i = this.Table.getSelectedRow();
        if (i > -1) {
            this.CourseEditor.onRowDeleted(i);
            this.model.removeRow(i);            
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Please select the course to be deleted", "Row not selected", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
    
