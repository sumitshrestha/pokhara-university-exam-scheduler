    public BackCandidateForm(java.awt.Frame parent, final String ID, final java.util.Map<CourseCode,String> grpCourses ) {
        this( parent, ID );
        try{
            this.CourseEditor.addSelectCourses(grpCourses);            
        }
        catch( Exception e ){
            this.ERROR_MESSAGE = e.getMessage();
        }
    }
    
    public BackCandidateForm(java.awt.Frame parent, final Candidate ID, final java.util.Map<CourseCode,String> grpCourses ) {
        this( parent, ID.getCandidateID(), grpCourses );
        java.util.List<CourseCode> BkCourses = (java.util.List)ID.getBackCourses();
        this.CourseEditor.addCourses( BkCourses );
        java.util.Iterator<CourseCode> BkCourseItr = BkCourses.iterator();
        while( BkCourseItr.hasNext() ){
            this.model.addRow(new Object[]{BkCourseItr.next()} );            
        }
    }
    
    /** @return the return status of this dialog - one of RET_OK or RET_CANCEL */
