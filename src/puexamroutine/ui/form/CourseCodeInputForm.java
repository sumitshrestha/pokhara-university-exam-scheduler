private void prefixTextKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_prefixTextKeyPressed
        if( evt.getKeyChar() == CourseCode.PrimarySeparator ){
            this.CourseText.requestFocusInWindow();
        }
        else
            if( evt.getKeyChar() == java.awt.event.KeyEvent.VK_ESCAPE ){
                this.onCancel();
            }
}//GEN-LAST:event_prefixTextKeyPressed

private void CourseTextKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CourseTextKeyPressed
    if( evt.getKeyChar() == CourseCode.SecondarySeparator ){
        this.creditText.requestFocusInWindow();
    }
    else
            if( evt.getKeyChar() == java.awt.event.KeyEvent.VK_ESCAPE ){
                this.onCancel();
            }
            else
                if( evt.getKeyChar() == java.awt.event.KeyEvent.VK_BACK_SPACE ){
                    if( this.CourseText.getText().length() == 0 ){
                        this.prefixText.requestFocusInWindow();
                        this.prefixText.setCaretPosition( this.prefixText.getText().length() -1 );                        
                    }
                }
}//GEN-LAST:event_CourseTextKeyPressed

