    public void initialize( CourseCode c ){
            this.prefixText.setValue( null );
            this.CourseText.setValue( null );
            this.creditText.setValue( null );
        if( c != null ){
            this.prefixText.setText( c.getPrefix() );
            this.CourseText.setText( c.getCode() );
            this.creditText.setText( c.getCourseCredit() );
        }
            this.prefixText.requestFocusInWindow();
    }
    
    public void initialize( CourseCode c, java.util.Map<CourseCode,String> courses ) throws Exception{
        this.setExistingCourseList( courses );
        this.initialize(c);
    }
    
    private void setExistingCourseList( java.util.Map<CourseCode,String> c ) throws Exception{
        if( c == null) 
            throw new Exception( "Course List is Empty" );
        if( c.isEmpty() ){
            throw new Exception( "No Course is present in this Group.\nPlease use Course Panel to input the course first." );
        }
        this.selectCourses.removeAllItems();
        this.selectCourses.setEnabled( true );
        
        java.util.Iterator<CourseCode> cItr = this.sort(c).iterator();
        
        while( cItr.hasNext() ){
            CourseCode temp = cItr.next();            
            String text = temp.toString();
            if( ! ( c.get( temp ) == null || c.get( temp ).equals("") ) ){
                text += "("+c.get(temp)+")";
            }
            this.selectCourses.addItem( text );
        }
        
        // since existing course is inputted, so, allow user to only select from it n not actually create new course from here.
        this.prefixText.setEditable( false );
        this.CourseText.setEditable( false );
        this.creditText.setEditable( false );
    }
    
    private ArrayList<CourseCode> sort( Map<CourseCode,String> c ){
        
        HashSet<String> uniqueCourses = getUniqueCourses(c);
        
        Object[] courses = uniqueCourses.toArray();        
        
        Arrays.sort(courses);
        
        ArrayList<CourseCode> list = new ArrayList<CourseCode>();
        for( int i=0; i<courses.length; i++ ){
            list.addAll( this.getCourseCodes( courses[i].toString(), c ));
        }
        
        return list;
    }
    
    private final Collection<CourseCode> getCourseCodes( String Course, java.util.Map<CourseCode,String> c ){
        Iterator<CourseCode> courseItr = c.keySet().iterator();
        java.util.Collection<CourseCode> ret = new java.util.HashSet<CourseCode>();
        while( courseItr.hasNext() ){
            CourseCode temp = courseItr.next();
            if( c.get( temp ).equals( Course ) ){
                ret.add(temp);
            }
        }
        return ret;
    }
    
    private final HashSet<String> getUniqueCourses(Map<CourseCode, String> c) {
        java.util.Iterator<String> temp = c.values().iterator();
        java.util.HashSet<String> uniqueCourses = new java.util.HashSet<String>();
        while (temp.hasNext()) {
            String t = temp.next();
            if (!uniqueCourses.contains(t)) {
                uniqueCourses.add(t);
            } else {
                continue;
            }
        }

        return uniqueCourses;
    }

    public final CourseCode getCourseCode(){
        CourseCode c = new CourseCode();
        c.setPrefix( this.prefixText.getValue().toString() );
        c.setCode( this.CourseText.getValue().toString() );
        c.setCourseCredit( this.creditText.getValue().toString() );
        return c;
    }
    
    private boolean commit(){
        try{            
            this.CourseText.commitEdit();
            this.prefixText.commitEdit();
            this.creditText.commitEdit();
            return true;
        }
        catch( Exception e ){
            JOptionPane.showMessageDialog( null, "Wrong Value was inputted\n"+e.getMessage(), "Input Error", JOptionPane.ERROR_MESSAGE );
            return false;
        }
    }
    
    public final int getReturnStatus(){
        return this.status;
    }
    
    private void onClose(){
        if( this.commit() ){
            this.status = this.OK;
            this.setVisible( false );
        }
    }
    
    private void onCancel(){        
            this.status = this.CANCEL;
            this.setVisible( false );        
    }
    
