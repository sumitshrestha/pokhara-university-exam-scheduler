    private void addRow() {
        if( this.ERROR_MESSAGE != null ){
            javax.swing.JOptionPane.showMessageDialog(null, this.ERROR_MESSAGE, "Course Code Not present", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (this.isFilled()) {
            Object[] Value = {++this.courseCounter, "", 0};
            this.TableModel.addRow(Value);            
        }
    }

    private void delRow() throws HeadlessException {
        int index = this.Table.getSelectedRow();
        if (index > -1) {
            this.TableModel.removeRow(index);
            this.courseCounter--;
            this.CourseEditor.onRowDeleted(index);
        } else {
            JOptionPane.showMessageDialog(null, "Please select row", "row not selected", JOptionPane.ERROR_MESSAGE);
        }
    }
    
