    private void TableKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TableKeyPressed
        final int DOWN_KEY = 40;
        if( evt.getKeyChar() == java.awt.event.KeyEvent.VK_DELETE ){
            this.delRow();
            return;
        }
        if( this.Table.getSelectedRow() < this.Table.getModel().getRowCount() - 1 ){            
            return;
        }
        if( evt.getKeyCode() == DOWN_KEY ){
            this.addRow();
        }   
    }//GEN-LAST:event_TableKeyPressed
    
    private void doClose(int retStatus) {
        returnStatus = retStatus;
        setVisible(false);
        dispose();
    }
    
    private boolean isFilled(){
        return Table.getRowCount() == 0 || !this.Table.getValueAt( Table.getRowCount() -1, 1 ).equals("");
    }
    
    public HashMap<CourseCode,Integer> getCourses(){
        try{
            HashMap<CourseCode,Integer> pair = new HashMap<CourseCode,Integer>();
            for( int i=0; i<this.CourseEditor.getForms().size(); i++ ){
                int net = Integer.parseInt( this.TableModel.getValueAt(i, 2).toString() );
                pair.put( this.CourseEditor.getForms().get(i), net );
            }
            return pair;
        }
        catch( Exception e ){
            return null;
        }
    }
    
    public Semester getRegularCourses(){
        HashMap pair = this.getCourses();
        Semester sem = new Semester( this.sem.getText(),new College(this.college.getText()), new Program(this.prg.getText()));        
        sem.setRegularCourses( new RegularCourses(sem.getCollege(),sem.getProgram(), sem, pair ) );
        return sem;
    }
    
