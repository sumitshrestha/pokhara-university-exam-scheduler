    private void addRow() {
        if (this.isFilled()) {
            Object[] data = {"", ""};
            this.model.addRow(data);
            this.BackCoursesEditor.onRowAddition( null );
        }
    }

    private boolean delRow() throws HeadlessException {
        int index = this.Table.getSelectedRow();
        if (index < 0) {
            javax.swing.JOptionPane.showMessageDialog(null, "Please select the candidate to be deleted", "Row not Selected", javax.swing.JOptionPane.ERROR_MESSAGE);
            return true;
        }
        this.model.removeRow(index);
        this.BackCoursesEditor.onRowDeleted(index);        
        this.BackCandidateSemesterEditor.onRowDeleted(index);
        return false;
    }
    
