    private boolean isFilled(){
        return this.Table.getRowCount()<1 || !this.Table.getValueAt( this.Table.getRowCount()-1, 0 ).equals("");
    }
    
    public CollegeBackCandidates getBackCandidates(){
        CollegeBackCandidates Candidates = new CollegeBackCandidates( new College( this.CollegeCombo.getSelectedItem().toString() ), new Program (this.PrgCombo.getSelectedItem().toString()) );
        java.util.List<BackCandidateForm> formList = this.BackCoursesEditor.getForms();
        for( int i=0; i<formList.size(); i++){
            BackCandidateForm form = formList.get(i);
            int sem = Integer.parseInt( this.Table.getValueAt(i, 1 ).toString() );
            Candidate cand = new Candidate( this.Table.getValueAt(i, 0 ).toString(), Candidates.getProgram(), Candidates.getCollege() , sem ,(sem==Candidate.NON_REGULAR)?puexamroutine.control.domain.interfaces.CandidateInterface.BACK_ONLY:Candidate.REGULAR_WITH_BACK );
            cand.addBackPapers( java.util.Arrays.asList(form.getCourses()) );
            Candidates.addBackPaperCandidate(cand);
            System.out.println("This is sem of "+cand.getCandidateID()+" "+sem);
        }
        return Candidates;        
    }
    
