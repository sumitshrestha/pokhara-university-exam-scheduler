    private void addRow() {
        if (this.isFilled()) {
            Object[] array = {""};
            this.model.addRow(array);
        }
    }

    private void delRow() throws HeadlessException {
        int index = this.Table.getSelectedRow();
        if (index > -1) {
            this.editor.onRowDeleted(index);
            this.model.removeRow(index);            
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Please select program to be deleted", "Program not Selected...", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
    
