    public ProgramForm(java.awt.Frame parent, final ProgramList list, String[] c, java.util.Map<CourseCode,String> courses ){
        this( parent, list.getGroup().getFaculty(), list.getGroup().getLevel(), list.getGroup().getDiscipline(), c, courses );
        this.editor.addGroupPrograms(list);
        
        java.util.ListIterator<Program> prgItr = list.getPrograms().listIterator();
        while( prgItr.hasNext() ){
            Program prg = prgItr.next();
            java.util.Iterator<College> collItr = prg.getSupportingColleges().iterator();
            String Colleges = "";
            while( collItr.hasNext() ){
                Colleges += collItr.next().getCollegeName() + ",";                
            }
            Colleges = Colleges.substring( 0, Colleges.length() -1 );//remove last ','
            
            this.model.addRow( new Object[]{ prg.getProgramName(), Colleges } );
        }
    }    
    
