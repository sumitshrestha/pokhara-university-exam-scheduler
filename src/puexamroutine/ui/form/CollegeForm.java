    public void setProgram(final String Program) {
        if( this.ProgramCombo.getItemCount() == 0 )
            this.ProgramCombo.addItem(Program);
        else{
            this.ProgramCombo.removeItemAt( this.ProgramCombo.getSelectedIndex() );
            this.ProgramCombo.addItem( Program );
        }
        this.RegEditor.setProgram(Program);
    }
    
