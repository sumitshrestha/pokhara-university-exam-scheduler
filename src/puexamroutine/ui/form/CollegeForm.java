    public CollegeForm(java.awt.Frame parent, final Program Program, final College coll, String[] centrelist, java.util.Map<CourseCode,String> courses ) {
        this( parent, Program.getProgramName(), centrelist, courses );
        this.CollegeText.setText( coll.getCollegeName() );
        java.util.List<RegularCourses> regs = coll.getRegularCourse(Program);
        java.util.List<CandidateInterface> cands = java.util.Arrays.asList( coll.getBackCandidates(Program).getBackPaperCandidates().toArray(new CandidateInterface[]{} ) );
        
        this.RegEditor.addRegularSemesters( regs );
        this.BackEditor.addBackCandidates(cands);
        
        java.util.Iterator<RegularCourses> regsItr = regs.iterator();
        while( regsItr.hasNext() ){
            RegularCourses reg = regsItr.next();
            this.RegTableModel.addRow( new Object[]{reg.getSemester().getSemester(), this.RegEditor.toString( reg.getCourses().toArray() ) } );
        }
        
        java.util.ListIterator<CandidateInterface> candItr = cands.listIterator();
        while( candItr.hasNext() ){
            CandidateInterface cand = candItr.next();
            this.BackTableModel.addRow( new Object[]{ cand.getCandidateID(), this.BackEditor.toString( cand.getBackPapers() ) } );
        }
        
    }
    
