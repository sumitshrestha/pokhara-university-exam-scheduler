    public CollegeBackCandidates getBackCandidates( Program prg, College c ){
        CollegeBackCandidates Candidates = new CollegeBackCandidates( c, prg);
        java.util.List<BackCandidateForm> formList = this.BackEditor.getForms();
        for( int i=0; i<formList.size(); i++){
            BackCandidateForm form = formList.get(i);
            Candidate cand = new Candidate( this.BackTable.getValueAt(i, 0 ).toString(), prg, c ,puexamroutine.control.domain.interfaces.CandidateInterface.NON_REGULAR  ,puexamroutine.control.domain.interfaces.CandidateInterface.BACK_ONLY  );
            cand.addBackPapers( java.util.Arrays.asList(form.getCourses()) );
            Candidates.addBackPaperCandidate(cand);
        }
        return Candidates;        
    }
    
    public SemesterList getAddedSemesters( Program prg, College c ){
        SemesterList list = new SemesterList( c, prg );
        java.util.Iterator<RegularCourseForm> formItr = this.RegEditor.getForms().iterator();
        while( formItr.hasNext() ){
            list.addSemester( formItr.next().getRegularCourses() );
        }
        return list;
    }
    
    public final College getCollege( Program prg ){
        College c = new College( this.getCollegeName() );
        c.addPrograms_Center_Semesters_Back( prg, this.getCentre(), this.getAddedSemesters(prg,c), this.getBackCandidates(prg,c));
        return c;
    }
    
