private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    GroupInputForm form = new GroupInputForm( null, this.controller.getGroupList().getGroups() );
    form.setVisible( true );
    if( form.getReturnStatus() == form.RET_OK ){
        Group grp = form.getInputtedGroup();
        boolean st = this.controller.getDatabaseManager().addGroup( grp.getFaculty(), grp.getLevel(), grp.getDiscipline() );
        if( ! st ){
            JOptionPane.showMessageDialog(null, grp.toString()+ " could not be added", "Error Group Addition", JOptionPane.ERROR_MESSAGE );
        }
        else{
            ((DefaultTableModel)this.GrpTable.getModel()).addRow( new Object[]{ grp.getFaculty(), grp.getLevel(), grp.getDiscipline() } );
        }
    }
}//GEN-LAST:event_jButton1ActionPerformed

private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    int row = this.GrpTable.getSelectedRow();
    if( row == -1 ){
        JOptionPane.showMessageDialog( null, "Row is not Selected. Please select the row/Group to be delected", "Row Not Selected", JOptionPane.ERROR_MESSAGE );
        return;
    }
    if( JOptionPane.showConfirmDialog( null, "Are you sure you want to delete the selected Group") != JOptionPane.OK_OPTION ){
        return;
    }
    boolean st = this.controller.getDatabaseManager().deleteDiscipline( this.GrpTable.getValueAt(row, 0).toString(), this.GrpTable.getValueAt(row, 1).toString(), this.GrpTable.getValueAt(row, 2).toString() );
    if( st ){
        ((DefaultTableModel)this.GrpTable.getModel()).removeRow(row);
    }
    else
        JOptionPane.showMessageDialog( null, "The Group Could not be deleted", "Group Not deleted", JOptionPane.ERROR_MESSAGE );
}//GEN-LAST:event_jButton2ActionPerformed

