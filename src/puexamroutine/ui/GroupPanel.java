    public void update() {
        while (this.GrpTable.getRowCount() > 0) {
            ((DefaultTableModel) this.GrpTable.getModel()).removeRow(0);
        }
        java.util.Iterator<Group> grpItr = this.controller.getGroupList().getGroups().iterator();
        while (grpItr.hasNext()) {
            Group grp = grpItr.next();
            ((DefaultTableModel) this.GrpTable.getModel()).addRow(new Object[]{grp.getFaculty(), grp.getLevel(), grp.getDiscipline()});
        }
    }

