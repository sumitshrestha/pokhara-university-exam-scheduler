    private final DefaultMutableTreeNode getSelectednode(){           
        return (DefaultMutableTreeNode) this.Tree.getLastSelectedPathComponent();
    }
    
    private void displayPopupMenu(final java.awt.event.MouseEvent evt, final javax.swing.JPopupMenu Menu ) {
        Menu.show(evt.getComponent(),evt.getX(), evt.getY());
    }

    private void onMenuClick(MouseEvent evt) {
        switch (this.Tree.getSelectionPath().getPathCount()) {
            case ROOT_NODE:
                this.displayPopupMenu(evt, RootMenu);
                break;
            case CENTRE_NODE:
                this.displayPopupMenu(evt, this.CentreMenu);
                break;
            case LIMIT_NODE:
                this.displayPopupMenu(evt, this.LimitMenu);
                break;
        }
    }
    
    private final boolean selectClickedNode(MouseEvent evt) {
        TreePath SelectedPath = this.Tree.getClosestPathForLocation(evt.getX(), evt.getY());
        if (SelectedPath == null) {
            return true;
        }
        if (this.Tree.getPathBounds(SelectedPath).contains(evt.getX(), evt.getY())) {            
            this.Tree.setSelectionPath(SelectedPath);
        } else {
            return true;
        }
        return false;
    }
    
    private final boolean isMenuClick( java.awt.event.MouseEvent evt ){
        return evt.getButton() == evt.BUTTON3;
    }
    
    private void addCentre(){
        ExamCentreForm form = new ExamCentreForm( null );
        form.setVisible( true );
        if ( form.getReturnStatus() == form.RET_OK ){
            CentreIdentifier c = form.getCentre();
            if( this.Writer.addCentre( c.getCentreName(), c.getMaximumCentreLimit() + "" ) )
                this.addCentre(c);            
            else
                JOptionPane.showMessageDialog( null, c.getCentreName()+" could not be written" , "Error", JOptionPane.ERROR_MESSAGE );
        }
    }
    
    private void updateCentreName(){
        DefaultMutableTreeNode node = this.getSelectednode();
        final String cent = node.toString();
        CentreUpdateForm form = new CentreUpdateForm( null, cent );
        form.setVisible( true );
        if( form.getReturnStatus() == form.RET_OK ){
            final String newCent = form.getNewCentre();            
            if( this.Writer.updateCentreName(cent, newCent) ){
                this.updateNode(node, newCent);
            }
            else
                JOptionPane.showMessageDialog( null, "newCent cannot be written", "Error", JOptionPane.ERROR_MESSAGE );
        }
        else
            System.out.println("from else condn");
    }
    
    private void updateCentreLimit(){
        DefaultMutableTreeNode node = this.getSelectednode();
        final String limit = node.toString();
        final String cent = node.getParent().toString();
        CentreLimitUpdateForm form = new CentreLimitUpdateForm( null, cent, limit );
        form.setVisible( true );
        if( form.getReturnStatus() == form.RET_OK ){
            final String newLimit = form.getNewLimit()+ "";
            if( this.Writer.updateCentreLimit( cent, newLimit)){
                this.updateNode(node, newLimit );
            }
            else
                JOptionPane.showMessageDialog( null, cent+" could not be updated", "Error", JOptionPane.ERROR_MESSAGE );
        }
    }
    
    private boolean updateNode( DefaultMutableTreeNode node, final String NewVal ){
        try{
            node.setUserObject( NewVal );
            this.Tree.updateUI();
            return true;
        }
        catch( Exception e ){
            return false;
        }
    }
    
