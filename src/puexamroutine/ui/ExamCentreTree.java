    public void setTreeList( CentreTable table ){
        this.Root.removeAllChildren();
        this.Tree.updateUI();
        Iterator<CentreIdentifier> centreItr = table.getCentres().iterator();
        while( centreItr.hasNext() ){
            this.addCentre( centreItr.next() );
        }
    }
    
    public void addCentre( CentreIdentifier centre ){
        DefaultMutableTreeNode centNode = new DefaultMutableTreeNode( centre.getCentreName() );
        DefaultMutableTreeNode maxLimitNode = new DefaultMutableTreeNode( centre.getMaximumCentreLimit() );
        centNode.add( maxLimitNode );
        this.Model.insertNodeInto(centNode, Root, 0 );
    }
    
